package com.po;

public class Attendance {
    private Integer attenId;
    private Integer attenTimes;
    private Integer teaId;

    public Integer getAttenId() {
        return attenId;
    }

    public void setAttenId(Integer attenId) {
        this.attenId = attenId;
    }

    public Integer getAttenTimes() {
        return attenTimes;
    }

    public void setAttenTimes(Integer attenTimes) {
        this.attenTimes = attenTimes;
    }

    public Integer getTeaId() {
        return teaId;
    }

    public void setTeaId(Integer teaId) {
        this.teaId = teaId;
    }
}